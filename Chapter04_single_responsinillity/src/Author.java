public class Author {
    private int authorId;
    private String name;

    // Getters and Setters
    public int getAuthorId() {
        return authorId;
 }
public void setAuthorId(int authorId) {
        this.authorId = authorId;
 }
public String getName() {
        return name;
 }
public void setName(String name) {
        this.name = name;
   }
}
